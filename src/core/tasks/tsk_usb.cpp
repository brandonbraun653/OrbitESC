/*-----------------------------------------------------------------------------
Includes
-----------------------------------------------------------------------------*/
#include <Chimera/thread>
#include <src/core/hw/orbit_usb.hpp>
#include <src/core/tasks.hpp>
#include <src/core/tasks/tsk_usb.hpp>
#include <tusb.h>

namespace Orbit::Tasks::USB
{
  /*---------------------------------------------------------------------------
  Public Functions
  ---------------------------------------------------------------------------*/
  void USBThread( void *arg )
  {
    /*-------------------------------------------------------------------------
    Wait for the start signal
    -------------------------------------------------------------------------*/
    waitInit();

    /*-------------------------------------------------------------------------
    Run the USB thread
    -------------------------------------------------------------------------*/
    Orbit::USB::powerUp();

    while ( 1 )
    {
      tud_task();
      tud_cdc_write_flush();
    }
  }
}    // namespace Orbit::Tasks::USB
